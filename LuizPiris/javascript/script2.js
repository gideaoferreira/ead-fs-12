// Loop "for":

for (let i = 0; i < 2; i++) {
    console.log("i = ", i);
}


// Loop "do" e "while":

let j = 0;
do {
    j++;
    console.log("j = ", j);
} while (j < 2);

// A ordem dos loops "do" e "while" faz diferença, pois o "do" executa o bloco de código ao menos uma vez antes de verificar a condição do "while".

let k = 0; 
do {
    console.log("k = ", k);
    k++;
} while (k < 2);


